/*

Difference between Set and Unordered Set
-------------------------------------------------------------------------------------------------------------------------------------|
    SET                                              |          UNORDERED SET                                                        |
-----------------------------------------------------|-------------------------------------------------------------------------------|
                                                     |                                                                               |
1. Set stores elements in a sorted order	         |       Unordered Set stores elements in an unsorted order                      |
-------------------------------------------------------------------------------------------------------------------------------------|
2. Set stores/acquire unique elements only	         |       Unordered Set stores/acquire only unique values                         |
-------------------------------------------------------------------------------------------------------------------------------------|
3. Set uses Binary Search Trees for implementation	 |       Unordered Set uses Hash Tables for implementation                       |
-------------------------------------------------------------------------------------------------------------------------------------|
4. More than one element can be erased by giving     |                                                                               |
the starting and ending iterator	                 |       We can erase that element for which the iterator position is given      |
-------------------------------------------------------------------------------------------------------------------------------------|
5. set<datatype> Set_Name;	                         |       unordered_set<datatype> UnorderedSet_Name;                              |
--------------------------------------------------------------------------------------------------------------------------------------


*/